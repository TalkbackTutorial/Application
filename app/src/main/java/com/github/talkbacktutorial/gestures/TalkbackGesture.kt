package com.github.talkbacktutorial.gestures

enum class TalkbackGesture {

    // Open the TalkBack menu
    DOWN_RIGHT,
    UP_RIGHT,
    TAP_3,

    // Pause or resume reading
    TAP_2,

    // Scroll
    UP_2,
    DOWN_2,
    LEFT_2,
    RIGHT_2,

    // Reading controls
    UP_DOWN,
    DOWN_UP,
    UP_3,
    DOWN_3,
    LEFT_3,
    RIGHT_3,

    // Start reading continuously
    TRIPLE_TAP_2,

    // Travel reading control items
    DOWN,
    UP,

    // Elements
    LEFT,
    RIGHT,
    DOUBLE_TAP,

    // Navigation
    DOWN_LEFT,
    UP_LEFT,
    LEFT_UP,
    RIGHT_DOWN,

    // Media
    DOUBLE_TAP_2,
    RIGHT_LEFT,
    LEFT_RIGHT,

    // Other
    LEFT_DOWN,
    RIGHT_UP,

    // None
    NO_MATCH,

}